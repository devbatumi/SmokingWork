export function currentStage(sinceMs: number): Stage {
  const h = sinceMs / (3600 * 1000);
  let cur = STAGES[0];
  for (const s of STAGES) {
    if (h >= s.atHours) cur = s;
    else break;
  }
  return cur;
}

export function nextStage(sinceMs: number): Stage | null {
  const h = sinceMs / (3600 * 1000);
  for (const s of STAGES) {
    if (h < s.atHours) return s;
  }
  return null;
}
